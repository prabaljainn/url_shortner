package com.urls.model;

public class UrlRequest {
	private String fullUrl;

	public UrlRequest() {
		super();
	}

	public UrlRequest(String fullUrl) {
		super();
		this.fullUrl = fullUrl;
	}

	public String getFullUrl() {
		return fullUrl;
	}

	public void setFullUrl(String fullUrl) {
		this.fullUrl = fullUrl;
	}

}
